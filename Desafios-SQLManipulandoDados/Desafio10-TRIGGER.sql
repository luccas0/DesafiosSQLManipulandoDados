CREATE TRIGGER TG_CLIENTES_IDADE ON CLIENTES
AFTER INSERT, UPDATE, DELETE
AS
BEGIN
SELECT CPF, IDADE, DATA_NASCIMENTO, 
    DATEDIFF(YEAR, DATA_NASCIMENTO, GETDATE()) 
FROM CLIENTES
END;

