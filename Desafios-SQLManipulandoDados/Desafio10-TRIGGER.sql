CPF, NOME, ENDERECO,
BAIRRO, CIDADE, ESTADO,
CEP, DATA_NASCIMENTO, IDADE,
GENERO, LIMITE_CREDITO, VOLUME_COMPRA,
PRIMEIRA_COMPRA
)
